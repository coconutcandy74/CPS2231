public class Courses {
    private String name;
    private String code;
    private String content;

    public Courses(String name, String code, String content) {
        this.name = name;
        this.code = code;
        this.content = content;
    }

    public String getName() {
        return name;
    }

    public String getCode() {
        return code;
    }

    public String getContent() {
        return content;
    }


}
